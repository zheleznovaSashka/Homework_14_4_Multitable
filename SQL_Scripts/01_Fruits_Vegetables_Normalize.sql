USE [Овощи_и_фрукты];
GO

-- ============================================================
-- 1. ПЕРВАЯ НОРМАЛЬНАЯ ФОРМА (1НФ)
-- ============================================================
-- Убеждаемся, что все поля атомарны (разбиты до минимальных значений)
-- В исходной структуре все поля атомарны, 1НФ соблюдена.

-- ============================================================
-- 2. ВТОРАЯ НОРМАЛЬНАЯ ФОРМА (2НФ)
-- ============================================================
-- Выносим Цвет и Тип в отдельные таблицы

-- Таблица "Типы" (овощ, фрукт, ягода и т.д.)
CREATE TABLE Типы (
    ТипID INT IDENTITY(1,1) PRIMARY KEY,
    НазваниеТипа NVARCHAR(20) NOT NULL UNIQUE
);
GO

-- Таблица "Цвета"
CREATE TABLE Цвета (
    ЦветID INT IDENTITY(1,1) PRIMARY KEY,
    НазваниеЦвета NVARCHAR(30) NOT NULL UNIQUE
);
GO

-- ============================================================
-- 3. ТРЕТЬЯ НОРМАЛЬНАЯ ФОРМА (3НФ)
-- ============================================================
-- Создаём основную таблицу с внешними ключами
-- (Калорийность и Описание зависят от продукта, а не от типа или цвета)

CREATE TABLE Продукты (
    ПродуктID INT IDENTITY(1,1) PRIMARY KEY,
    Название NVARCHAR(50) NOT NULL UNIQUE,
    ТипID INT NOT NULL,
    ЦветID INT NOT NULL,
    Калорийность INT NOT NULL,
    Описание NVARCHAR(200) NULL,
    CONSTRAINT FK_Продукты_Тип FOREIGN KEY (ТипID) REFERENCES Типы(ТипID),
    CONSTRAINT FK_Продукты_Цвет FOREIGN KEY (ЦветID) REFERENCES Цвета(ЦветID)
);
GO

-- ============================================================
-- 4. ПЕРЕНОС ДАННЫХ
-- ============================================================

-- Заполняем таблицу "Типы"
INSERT INTO Типы (НазваниеТипа)
SELECT DISTINCT Тип FROM Овощи_и_фрукты;
GO

-- Заполняем таблицу "Цвета"
INSERT INTO Цвета (НазваниеЦвета)
SELECT DISTINCT Цвет FROM Овощи_и_фрукты;
GO

-- Заполняем основную таблицу "Продукты"
INSERT INTO Продукты (Название, ТипID, ЦветID, Калорийность, Описание)
SELECT
    f.Название,
    t.ТипID,
    c.ЦветID,
    f.Калорийность,
    f.Описание
FROM Овощи_и_фрукты f
JOIN Типы t ON f.Тип = t.НазваниеТипа
JOIN Цвета c ON f.Цвет = c.НазваниеЦвета;
GO

-- ============================================================
-- 5. ПРОВЕРКА ДАННЫХ
-- ============================================================

SELECT TOP 1000 * FROM Продукты;
GO

-- ============================================================
-- 6. ЗАПРОСЫ С ДВУМЯ И БОЛЕЕ ТАБЛИЦАМИ
-- ============================================================

-- Запрос 1: Все продукты с названиями типа и цвета
SELECT
    p.Название AS Продукт,
    t.НазваниеТипа AS Тип,
    c.НазваниеЦвета AS Цвет,
    p.Калорийность,
    p.Описание
FROM Продукты p
JOIN Типы t ON p.ТипID = t.ТипID
JOIN Цвета c ON p.ЦветID = c.ЦветID
ORDER BY p.Название;
GO

-- Запрос 2: Количество продуктов по типам
SELECT
    t.НазваниеТипа AS Тип,
    COUNT(*) AS Количество
FROM Продукты p
JOIN Типы t ON p.ТипID = t.ТипID
GROUP BY t.НазваниеТипа
ORDER BY Количество DESC;
GO

-- Запрос 3: Продукты с калорийностью выше среднего по их типу
SELECT
    p.Название,
    t.НазваниеТипа,
    p.Калорийность
FROM Продукты p
JOIN Типы t ON p.ТипID = t.ТипID
WHERE p.Калорийность > (
    SELECT AVG(Калорийность)
    FROM Продукты p2
    WHERE p2.ТипID = p.ТипID
)
ORDER BY t.НазваниеТипа, p.Калорийность DESC;
GO

-- Запрос 4: Самый калорийный продукт каждого цвета
SELECT
    c.НазваниеЦвета AS Цвет,
    p.Название AS Самый_калорийный,
    p.Калорийность
FROM Продукты p
JOIN Цвета c ON p.ЦветID = c.ЦветID
WHERE p.Калорийность = (
    SELECT MAX(Калорийность)
    FROM Продукты p2
    WHERE p2.ЦветID = p.ЦветID
)
ORDER BY c.НазваниеЦвета;
GO

-- Запрос 5: Средняя калорийность по типам и цветам
SELECT
    t.НазваниеТипа AS Тип,
    c.НазваниеЦвета AS Цвет,
    AVG(p.Калорийность) AS Средняя_калорийность
FROM Продукты p
JOIN Типы t ON p.ТипID = t.ТипID
JOIN Цвета c ON p.ЦветID = c.ЦветID
GROUP BY t.НазваниеТипа, c.НазваниеЦвета
ORDER BY t.НазваниеТипа, c.НазваниеЦвета;
GO